CREATE PROCEDURE [dbo].[UPD_BTRF_P]
	-- Add the parameters for the stored procedure here
	@Code     BIGINT,
	@BTRFDesc NVARCHAR(250),
	@BTRFCode BIGINT
AS
BEGIN
 	-- بررسی دسترسی کاربر
	DECLARE @AP BIT
	       ,@AccessString VARCHAR(250);
	SET @AccessString = N'<AP><UserName>' + SUSER_NAME() + '</UserName><Privilege>18</Privilege><Sub_Sys>10</Sub_Sys></AP>';	
   EXEC iProject.dbo.SP_EXECUTESQL N'SELECT @ap = DataGuard.AccessPrivilege(@P1)',N'@P1 ntext, @ap BIT OUTPUT',@AccessString , @ap = @ap output
   IF @AP = 0 
   BEGIN
      RAISERROR ( N'خطا - عدم دسترسی به ردیف 18 سطوح امینتی : شما مجوز ویرایش کردن سبک را ندارید', -- Message text.
               16, -- Severity.
               1 -- State.
               );
      RETURN;
   END
   -- پایان دسترسی
   
   UPDATE Base_Tariff
      SET BTRF_DESC = @BTRFDesc
         ,BTRF_CODE = @BTRFCode
    WHERE CODE = @Code;
END
GO
